import type { BlockShape } from "@/types/game";

/**
 * 블록 형태 정의 (2차원: 1 = 칸 존재, 0 = 빈칸)
 * 9x9 그리드에 맞춰 최대 9칸까지 사용 가능
 *
 * 인덱스 참조 (가로모드 드래그 고스트 방향 확인용, 총 27개, 인덱스 0~26):
 *  0: 1x1, 1: 2x1가로, 2: 2x1세로, 3: 3x1가로, 4: 3x1세로,
 *  5: ㄱ(왼위), 6: ㄱ(오위), 7: ㄴ(왼아래), 8: ㄴ(오아래),
 *  9: 2x2, 10: 4x1가로(I), 11: 4x1세로(I), 12: L, 13: J, 14: T,
 * 15: T세로, 16: Z, 17: S, 18: 큰L(3x2), 19: 작은L세로,
 * 20: 5x1가로, 21: 5x1세로, 22: 5칸L, 23: 5칸L반대, 24: +(플러스),
 * 25: T긴세로, 26: U자
 */
export const BLOCK_SHAPES: BlockShape[] = [
  // ---- 1칸 ----
  [[1]], // 1x1

  // ---- 2칸 ----
  [[1, 1]], // 2x1 가로
  [[1], [1]], // 2x1 세로

  // ---- 3칸 ----
  [[1, 1, 1]], // 3x1 가로
  [[1], [1], [1]], // 3x1 세로
  [
    [1, 1],
    [1, 0],
  ], // ㄱ (왼쪽 위)
  [
    [1, 1],
    [0, 1],
  ], // ㄱ (오른쪽 위)
  [
    [1, 0],
    [1, 1],
  ], // ㄴ (왼쪽 아래)
  [
    [0, 1],
    [1, 1],
  ], // ㄴ (오른쪽 아래)

  // ---- 4칸 ----
  [
    [1, 1],
    [1, 1],
  ], // 2x2 정사각형
  [[1, 1, 1, 1]], // 4x1 가로 (I)
  [[1], [1], [1], [1]], // 4x1 세로 (I)
  [
    [1, 1, 1],
    [1, 0, 0],
  ], // L
  [
    [1, 1, 1],
    [0, 0, 1],
  ], // J
  [
    [1, 1, 1],
    [0, 1, 0],
  ], // T
  [
    [1, 0],
    [1, 1],
    [1, 0],
  ], // T 세로
  [
    [1, 1, 0],
    [0, 1, 1],
  ], // Z
  [
    [0, 1, 1],
    [1, 1, 0],
  ], // S
  [
    [1, 1],
    [1, 1],
    [1, 0],
  ], // 큰 L (3x2)
  [
    [1, 1],
    [0, 1],
    [0, 1],
  ], // 작은 L 세로

  // ---- 5칸 ----
  [[1, 1, 1, 1, 1]], // 5x1 가로
  [[1], [1], [1], [1], [1]], // 5x1 세로
  [
    [1, 1, 1],
    [1, 1, 0],
  ], // 5칸 L
  [
    [1, 1, 1],
    [0, 1, 1],
  ], // 5칸 L 반대
  [
    [0, 1, 0],
    [1, 1, 1],
    [0, 1, 0],
  ], // + (플러스)
  [
    [1, 1, 1],
    [0, 1, 0],
    [0, 1, 0],
  ], // T 긴 세로
  [
    [1, 0, 0],
    [1, 1, 1],
    [1, 0, 0],
  ], // U 자
];

